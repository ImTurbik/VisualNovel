#pragma once
#include <string>
#include <vector>

/**
 * @brief Структура, представляющая вариант ответа игрока в диалоге.
 */
struct DialogueChoice {
    /** @brief Текст варианта ответа, который отображается игроку. */
    std::string text;
    
    /** @brief Идентификатор (ID) следующего узла диалога, к которому ведет этот выбор. */
    int nextNodeId;
};

/**
 * @brief Структура, представляющая отдельный узел (реплику) диалога.
 * @details Содержит текст реплики NPC, имя говорящего, а также логику перехода 
 *          к следующим узлам через варианты ответа или напрямую.
 */
struct DialogueNode {
    /** @brief Уникальный идентификатор (ID) данного узла диалога. */
    int id;
    
    /** @brief Имя персонажа (NPC), который произносит реплику. */
    std::string npcName;
    
    /** @brief Текст реплики персонажа. */
    std::string text;
    
    /** @brief Список доступных игроку вариантов ответа. */
    std::vector<DialogueChoice> choices;
    
    /** 
     * @brief ID следующего узла по умолчанию.
     * @note Используется, если узел не имеет вариантов ответа (choices пуст) 
     *       и диалог должен продолжиться автоматически (например, по клику).
     */
    int nextNodeId;
};