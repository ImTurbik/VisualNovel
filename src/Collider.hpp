#pragma once
#include <SDL3/SDL.h>

/**
 * @brief Класс для обработки двумерных столкновений (коллизий).
 * @details Обеспечивает хранение границ объекта в виде прямоугольника 
 *          и предоставляет статические методы для проверки их пересечения.
 */
class Collider {
public:
    /**
     * @brief Границы коллайдера (координаты и размеры).
     */
    SDL_FRect bounds;

    /**
     * @brief Конструктор для создания нового объекта Collider.
     * 
     * @param x Координата X верхнего левого угла.
     * @param y Координата Y верхнего левого угла.
     * @param w Ширина прямоугольника столкновения.
     * @param h Высота прямоугольника столкновения.
     */
    Collider(float x, float y, float w, float h);

    /**
     * @brief Проверяет пересечение двух прямоугольных областей.
     * 
     * @param rectA Первый прямоугольник для проверки.
     * @param rectB Второй прямоугольник для проверки.
     * @return true Если прямоугольники пересекаются (произошло столкновение).
     * @return false Если прямоугольники не пересекаются.
     */
    static bool checkCollision(const SDL_FRect& rectA, const SDL_FRect& rectB);

    /**
     * @brief Отрисовывает границы коллайдера для отладки.
     * 
     * @param renderer Указатель на активный SDL_Renderer, используемый для рендеринга.
     */
    void drawDebug(SDL_Renderer* renderer);
};