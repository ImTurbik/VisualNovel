/**
 * @file GameObject.cpp
 * @brief Реализация базового класса игрового объекта.
 */

#include "GameObject.hpp"
#include "TextureManager.hpp"

/**
 * @brief Инициализирует (загружает) начальные параметры игрового объекта.
 * 
 * Задает пространственные координаты, размеры, а также привязывает текстурный тег
 * и сбрасывает счетчики кадров анимации в исходное состояние.
 * 
 * @param tag Уникальный строковый идентификатор текстуры в TextureManager.
 * @param x Начальная координата объекта по оси X.
 * @param y Начальная координата объекта по оси Y.
 * @param w Ширина объекта на экране.
 * @param h Высота объекта на экране.
 */
void GameObject::load(std::string tag, float x, float y, float w, float h) {
    x_ = x;
    y_ = y;
    w_ = w;
    h_ = h;
    texture_tag_ = tag;
    currentRow_ = 1;   ///< Первая строка на спрайт-листе (по умолчанию)
    currentFrame_ = 0; ///< Начальный (нулевой) кадр анимации
}

/**
 * @brief Отрисовывает конкретный кадр анимации объекта на экране.
 * 
 * Метод делегирует отрисовку синглтону TextureManager, передавая текущую
 * строку и номер кадра для корректного вырезания под-прямоугольника из спрайт-листа.
 * 
 * @param renderer Указатель на активный контекст рендеринга SDL.
 */
void GameObject::draw(SDL_Renderer* renderer) {
    TextureManager::Instance().drawFrame(texture_tag_, x_, y_, w_, h_,
                                         currentRow_, currentFrame_, renderer);
}

/**
 * @brief Обновляет внутреннюю логику и состояние объекта за один кадр.
 * 
 * В базовом классе метод оставлен пустым. Он предназначен для переопределения
 * в дочерних классах (например, Player или NPC) для реализации физики,
 * перемещения и смены кадров анимации.
 */
void GameObject::update() {

}

/**
 * @brief Освобождает ресурсы, связанные с данным игровым объектом.
 * 
 * Используется для кастомной очистки памяти или вывода отладочной информации 
 * при уничтожении сущности.
 */
void GameObject::clean() {
    std::cout << "Clean game object" << std::endl;
}

/**
 * @brief Возвращает прямоугольные границы (хитбокс) объекта.
 * 
 * Используется системой коллизий для проверки пересечений с другими объектами 
 * или коллайдерами карты.
 * 
 * @return SDL_FRect Структура, содержащая текущие координаты и размеры объекта (float).
 */
SDL_FRect GameObject::getBounds() const
{
    return SDL_FRect{ x_, y_, w_, h_ };
}

/**
 * @brief Возвращает текстурный тег, привязанный к объекту.
 * 
 * Позволяет внешним системам (например, менеджеру коллизий в классе Game) 
 * узнать, какой графический ассет сейчас используется объектом.
 * 
 * @return std::string Строковый идентификатор текстуры.
 */
std::string GameObject::getTextureTag() const
{
    return texture_tag_;
}