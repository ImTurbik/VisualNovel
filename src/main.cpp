/**
 * @file main.cpp
 * @brief Точка входа в игру и реализация главного игрового цикла (Game Loop).
 */

#define SDL_MAIN_HANDLED
#include "Game.hpp"

/**
 * @brief Главная функция приложения.
 * 
 * Инициализирует основной объект игры, настраивает графическое окно и запускает
 * бесконечный цикл `while`, который выполняется до тех пор, пока игра не получит сигнал на выход.
 * После завершения цикла корректно освобождает все ресурсы.
 * 
 * @param argc Количество аргументов командной строки.
 * @param argv Массив аргументов командной строки.
 * @return int Код завершения программы (0 — успешное выполнение).
 */
int main(int argc, char* argv[]) {
    // Выделение памяти под центральный контроллер игрового движка
    Game* game = new Game();

    // Инициализация подсистем SDL, создание окна 1280x720 и загрузка базовых ассетов
    if (game->init("Visual Novel", 1280, 720)) {
        
        // === ГЛАВНЫЙ ИГРОВОЙ ЦИКЛ (GAME LOOP) ===
        // Выполняется каждую итерацию (каждый кадр), пока игра активна
        while (game->isRunning()) {
            
            // 1. Фаза ввода: опрос системных событий, кликов мыши и нажатий клавиш
            game->handleEvents();
            
            // 2. Фаза логики: обновление позиций объектов, расчет коллизий и анимаций
            game->update();
            
            // 3. Фаза рендеринга: очистка экрана и вывод новой графики/текста на дисплей
            game->render();
        }
    }

    // Деинициализация подсистем SDL и освобождение памяти внутренних менеджеров
    game->clean();
    
    // Удаление объекта игры для предотвращения утечек оперативной памяти
    delete game;
    
    return 0;
}